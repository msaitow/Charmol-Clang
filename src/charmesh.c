// main functions of mesh generator
// cleaning mesh
MESH mesh_clean_mesh(MESH mesh)
{
int i;

for(i=0;i<mesh.ntriangles;i++) {free(mesh.corners[i]); free(mesh.normals[i]);}
free(mesh.corners); mesh.corners = NULL;
free(mesh.normals); mesh.normals = NULL;
if(mesh.colors!=NULL)
 {
 for(i=0;i<mesh.ntriangles;i++) free(mesh.colors[i]);
 free(mesh.colors); mesh.colors = NULL;
 }
mesh.ntriangles = 0;

return mesh;
}

// generating mesh
MESH mesh_generate_mesh(int *gp, double *gd, double *gx, double *gy, double *gz, double *gval, double *gpot, double isoval)
{
int i, j, k;
double *cubecorval, **cubecorcoor, **cubecorgrad, **cubecorcol;
MESH mesh;

// initialization
mesh.ntriangles = 0; mesh.corners = NULL; mesh.normals = NULL; mesh.colors = NULL;
cubecorval = NULL; cubecorcoor = NULL; cubecorgrad = NULL; cubecorcol = NULL;

// allocate memory
if((cubecorval = (double *) malloc(8*sizeof(double)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
if((cubecorcoor = (double **) malloc(8*sizeof(double *)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
if((cubecorgrad = (double **) malloc(8*sizeof(double *)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
if((cubecorcol = (double **) malloc(8*sizeof(double *)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
for(i=0;i<8;i++)
 {
 if((cubecorcoor[i] = (double *) malloc(3*sizeof(double)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
 if((cubecorgrad[i] = (double *) malloc(3*sizeof(double)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
 if((cubecorcol[i] = (double *) malloc(3*sizeof(double)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
 }

// processing grid cube by cube
for(k=1; k < (gp[2] - 2) ;k++)
 {
 for(j=1; j < (gp[1] - 2) ;j++)
  {
  for(i=1; i < (gp[0] - 2) ;i++)
   {
   mesh = mesh_process_cube(gp, gd, gx, gy, gz, gval, gpot, isoval, i, j, k, cubecorval, cubecorcoor, cubecorgrad, cubecorcol, mesh);
   }
  }
 }

//deallocate memory
for(i=0;i<8;i++) {free(cubecorcoor[i]); free(cubecorgrad[i]); free(cubecorcol[i]);}
free(cubecorcoor); cubecorcoor = NULL;
free(cubecorgrad); cubecorgrad = NULL;
free(cubecorcol); cubecorcol = NULL;
free(cubecorval); cubecorval = NULL;

return mesh;
}

