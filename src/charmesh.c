// functions for mesh generation
// calculate gradients in cube corners
void mesh_calc_gradients(int *gp, double *gd, double *gval, int gi, int gj, int gk, double **cubecorgrad)
{
int i, j, indm, indp;
double len;

for(i=0;i<8;i++)
 {
 for(j=0;j<3;j++)
  {
  indm = (gi + cubecorvec[i][0] - coorvec[j][0])*gp[1]*gp[2] + (gj + cubecorvec[i][1] - coorvec[j][1])*gp[2] + (gk + cubecorvec[i][2] - coorvec[j][2]);
  indp = (gi + cubecorvec[i][0] + coorvec[j][0])*gp[1]*gp[2] + (gj + cubecorvec[i][1] + coorvec[j][1])*gp[2] + (gk + cubecorvec[i][2] + coorvec[j][2]);
  cubecorgrad[i][j] = (gval[indp] - gval[indm]) / 2.0*gd[j];
  }
 len = 0.0; for(j=0;j<3;j++) len += cubecorgrad[i][j]*cubecorgrad[i][j]; len = sqrt(len);
 for(j=0;j<3;j++) cubecorgrad[i][j] /= len;
 }
}

// calculate colors in cube corners
void mesh_calc_colors(int *gp, double *gpot, int gi, int gj, int gk, double **cubecorcol)
{
int i, j;
double energy, col[3];

for(i=0;i<8;i++)
 {
 for(j=0;j<3;j++) col[j] = 0.0;
 energy = gpot[(gi + cubecorvec[i][0])*gp[1]*gp[2] + (gj + cubecorvec[i][1])*gp[2] + (gk + cubecorvec[i][2])];
 if(energy<0.0)
  {
  col[0] = -20.0*energy;
  if(col[0]>=1.0) col[0] = 1.0;
   else col[1] = 1.0 - col[0];
  }
  else
   {
   if(energy>0.0)
    {
    col[2] = 20.0*energy;
    if(col[2]>=1.0) col[2] = 1.0;
     else col[1] = 1.0 - col[2];
    }
    else col[1] = 1.0;
   }
 for(j=0;j<3;j++) cubecorcol[i][j] = col[j];
 }
}

// process one cube
MESH mesh_process_cube(int *gp, double *gd, double *gx, double *gy, double *gz, double *gval, double *gpot, double isoval, int gi, int gj, int gk,
                       double *cubecorval, double **cubecorcoor, double **cubecorgrad, double **cubecorcol, MESH mesh)
{
int i, j, k, cubest, ntri, store, edge, coor, ind0, ind1;
double factor;
const int *acttricon;

// filling values in cube corners
for(i=0;i<8;i++)
 {
 j = (gi + cubecorvec[i][0])*gp[1]*gp[2] + (gj + cubecorvec[i][1])*gp[2] + (gk + cubecorvec[i][2]);
 cubecorval[i] = gval[j];
 }

// determining the cube state (one of the 256 possibilities) and
// number of triangles to make (leaving if no triangle should be made)
cubest = 0;
for(i=0;i<8;i++)
 {
 if(cubecorval[i]<isoval) cubest += corbinfac[i];
 }
acttricon = triangleconnect[cubest];
i = 0; while(acttricon[i]!=-1) i++;
if((i%3)!=0) {puts("\nThere is an error in triangleconnect table!!!\n"); exit(1);}
ntri = i/3;
if(ntri==0) return mesh;

// filling coordinates, calculating gradients and possibly calculating colors in cube corners
for(i=0;i<8;i++)
 {
 j = (gi + cubecorvec[i][0])*gp[1]*gp[2] + (gj + cubecorvec[i][1])*gp[2] + (gk + cubecorvec[i][2]);
 cubecorcoor[i][0] = gx[j];
 cubecorcoor[i][1] = gy[j];
 cubecorcoor[i][2] = gz[j];
 }
mesh_calc_gradients(gp, gd, gval, gi, gj, gk, cubecorgrad);
if(gpot!=NULL) mesh_calc_colors(gp, gpot, gi, gj, gk, cubecorcol);

// preparing mesh for new tringles
if((mesh.corners = (double **) realloc(mesh.corners, (mesh.ntriangles+ntri)*sizeof(double *)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
if((mesh.normals = (double **) realloc(mesh.normals, (mesh.ntriangles+ntri)*sizeof(double *)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
for(i=mesh.ntriangles;i<(mesh.ntriangles+ntri);i++)
 {
 if((mesh.corners[i] = (double *) malloc(9*sizeof(double)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
 if((mesh.normals[i] = (double *) malloc(9*sizeof(double)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
 }
if(gpot!=NULL)
 {
 if((mesh.colors = (double **) realloc(mesh.colors, (mesh.ntriangles+ntri)*sizeof(double *)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
 for(i=mesh.ntriangles;i<(mesh.ntriangles+ntri);i++)
  {
  if((mesh.colors[i] = (double *) malloc(9*sizeof(double)))==NULL) {puts("\nMemory allocation failed!\n"); exit(1);}
  }
 }

// calculating and storing coordinates, normals and possibly colors of new triangles
for(i=0;i<ntri;i++)
 {
 store = mesh.ntriangles + i;
 for(j=0;j<3;j++)
  {
  edge = acttricon[i*3+j];
  ind0 = edgeconnect[edge][0];
  ind1 = edgeconnect[edge][1];
  factor = (isoval - cubecorval[ind0]) / (cubecorval[ind1] - cubecorval[ind0]);
  for(k=0;k<3;k++)
   {
   coor = j*3 + k;
   mesh.corners[store][coor] = cubecorcoor[ind0][k] + (cubecorcoor[ind1][k] - cubecorcoor[ind0][k])*factor;
   mesh.normals[store][coor] = cubecorgrad[ind0][k] + (cubecorgrad[ind1][k] - cubecorgrad[ind0][k])*factor;
   if(!isfinite(mesh.normals[store][coor])) mesh.normals[store][coor] = 0.0; // calculation of gradient may fail in some cases
   if(gpot!=NULL) mesh.colors[store][coor] = cubecorcol[ind0][k] + (cubecorcol[ind1][k] - cubecorcol[ind0][k])*factor;
   }
  }
 }

// saving new number of triangles
mesh.ntriangles += ntri;

return mesh;
}

